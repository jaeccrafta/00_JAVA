public class GalNaLit {
    public static void main(String args[])
    {
		// program prevedie galony na litre
        double galony, litry;
        double pocetLitvGal = 3.7854;

        galony = 10;

        litry = galony * pocetLitvGal;

        System.out.println(galony + " galonů se rovná " + litry + " litrů.");
    }
}