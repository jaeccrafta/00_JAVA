// Program vypočítá moju váhu na Mesiaci
// Mesačná gravitácia odpoveda 17%-ám pozemskej

public class vahaNaMesiaci {
    public static void main(String args[])
    {
        double mojaVaha, mojaVahaMes;

        mojaVaha = 85.5;
        mojaVahaMes = mojaVaha * 0.17;


        System.out.println("Moja váha na Zemi: " + mojaVaha);
        System.out.println("Moja váha na Mesiaci: " + mojaVahaMes);
    }
}
